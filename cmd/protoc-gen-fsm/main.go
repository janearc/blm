// screamingSnake turns a CamelCase identifier into SCREAMING_SNAKE (BentoState ->
// BENTO_STATE), so the value-name prefix can be stripped to the short state name.
func screamingSnake(s string) string {
	var b strings.Builder
	for i, r := range s {
		if i > 0 && r >= 'A' && r <= 'Z' {
			b.WriteByte('_')
		}
		b.WriteRune(r)
	}
	return strings.ToUpper(b.String())
}

// camel turns a SCREAMING_SNAKE short state name into CamelCase (NOT_STARTED ->
// NotStarted) for the handler method name.
func camel(s string) string {
	var b strings.Builder
	for _, part := range strings.Split(s, "_") {
		if part == "" {
			continue
		}
		b.WriteString(strings.ToUpper(part[:1]))
		b.WriteString(strings.ToLower(part[1:]))
	}
	return b.String()
}
